--- Total cases vs Total Deaths
--Likelihood of contracting covid if you were in a particular country.

Select Location, date, Total_cases, total_deaths, (Total_deaths/Total_cases)*100 as DeathPercentage
From CovidDeaths
---Where total_deaths is NOT NULL
Order by 1, 2

---Total_cases vs Population in Canada
--Deep dive into what percentage of the Canadian population got covid.

Select Location, date, Total_cases, population, (Total_deaths/population)*100 as DeathPercentage
From CovidDeaths
Where location like '%Canada%'
order by 1,2

--- Looking at Countries with Highest Infection Rate compared to Population
Select Location, population, MAX(Total_cases) as highestInfectCount, MAX((Total_cases/population))*100 as PercentPopulationInfected
From CovidDeaths
--Where location like '%Canada%'
Group by location, Population
order by PercentPopulationInfected desc

-- Displaying Countries with the highest death count per population

Select Location, population, MAX(Total_deaths) as TotalDeathAmount
From CovidDeaths
--Where location like '%Canada%'
Group by location, Population
order by TotalDeathAmount desc

